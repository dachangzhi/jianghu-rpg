// ---- 后日谈系统 ----
SCENES['epilogue'] = () => {
  G.scene = 'epilogue';
  clearStory();
  chapterTitle('后日谈');
  
  narrate(`苍龙镇的雨停了。\n\n阳光穿透云层，照在湿漉漉的石板路上。\n镇子像是从一场噩梦中醒来——表面上一切如常，但每个人都知道，昨夜发生的事，会改变所有人的命运。`);

  divider();
  
  // 根据不同角色的好感度和flag展示不同的后日谈
  const epilogues = [];
  
  if (getRel('shen_guyan') > 20) {
    epilogues.push(() => {
      narrate(`\n${hl('沈孤雁')}站在客栈门口，目送你远去。\n\n他从怀中取出一本薄薄的册子——「拨云手」心法的抄本。\n\n「这是我能给你的最好的礼物。剑术之道，不在于杀，在于守。守住你心里最重要的东西。」\n\n你接过册子，翻了几页。每一页都是他亲手写的，字迹工整，注释详细。\n\n十五年守夜人的人生智慧，都浓缩在这一本小册子里了。`);
      addItem('拨云手心法');
      G.sword += 3;
    });
  }
  
  if (getRel('liu_ruyin') > 15) {
    epilogues.push(() => {
      narrate(`\n${hl('柳如烟')}在镇口的古树下等你。\n\n她换了一身素色衣裳，没有了锦衣卫的冷硬气质，看起来像是一个普通的年轻女子。\n\n「我要去岭南了。」她说，「去找我父亲。锦衣卫的身份——我已经不要了。从今以后，叫我柳如烟就好。不是百户，不是暗探——就只是柳如烟。」\n\n她从袖中取出那把青锋冷月剑，看了最后一眼，然后插入古树前的泥土中。\n\n「这把剑，替我杀了太多人。让它留在这里吧——当一个警示。」\n\n她转身南行，没有回头。但风吹起了她的衣角，你看到她的嘴角——微微上扬。`);
    });
  }
  
  if (getRel('zhao_tieniu') > 20) {
    epilogues.push(() => {
      narrate(`\n${hl('赵铁牛')}把一个沉甸甸的包裹塞给你。\n\n「打开看看。」\n\n里面是一把新打的短刀——刀身乌黑，但刃口闪着寒光。刀柄上刻着两个小字：「${hl('承志')}」。\n\n「这是我赵承志这辈子打得最好的一把刀。用了三天三夜。送给你——替我看看外面的世界。我走不了了，腿脚不行了。但我的刀可以替我走。」\n\n他拍了拍你的背，力道大得让你踉跄了一步。\n\n「去吧。有空回来看看老赵。」`);
      addItem('承志刀');
      G.sword += 2;
    });
  }
  
  if (getRel('bai_yunsheng') > 15) {
    epilogues.push(() => {
      narrate(`\n${hl('白云生')}在茶馆等你。他第一次点了茶而不是酒。\n\n「我决定了。」他说，「我要去京城。不是去考试——是去写书。写一本关于前朝遗臣的书——不是歌颂，不是批评——只是记录。让后人知道，这些人存在过。」\n\n他从怀里取出一支毛笔——那是白太傅的遗物。\n\n「爷爷用这支笔写了一辈子的文章。现在轮到我了。」\n\n他举起茶杯，对你微微一笑——没有醉意的、真实的、温暖的笑容。\n\n「后会有期，朋友。」`);
    });
  }
  
  if (hasFlag('hei_redemption')) {
    epilogues.push(() => {
      narrate(`\n你在镇外的山路上遇到了${hl('黑无极')}。他换了一身灰色衣裳，不再蒙面。\n\n伤痕累累的脸在阳光下看起来没那么可怕了。\n\n「你给了我一个选择。」他说，「我选了放下。暗星阁的人——我会一个一个去找他们，告诉他们——不用再躲了。」\n\n他从怀中取出一块令牌——暗星阁的最高令符。\n\n「这是唯一能命令所有暗星阁成员的信物。从今以后——它归你了。如果你需要帮忙——拿着它，他们会来。」\n\n他将令牌放在路边的石头上，然后转身走进了山林。\n\n背影不再像影子——像一个终于卸下重担的普通人。`);
      addItem('暗星阁令符');
    });
  }

  if (epilogues.length === 0) {
    narrate(`\n你没有和任何人告别。\n\n翻身上马，朝着远方驶去。\n\n身后是苍龙镇——一个你来了又走的地方。\n这里发生的事，会留在你心里。但此刻——\n\n路在脚下。江湖在远方。`);
  } else {
    epilogues.forEach(fn => fn());
  }

  divider();
  narrate(`${hl('江湖路远。\n\n但有些路，值得走。\n\n有些人，值得记。')}\n\n—— 全剧终 ——`);

  showChoices([
    { text: '查看结局统计', id: 'epilogue_stats', next: () => {
      narrate(`\n—— 结局统计 ——`);
      narrate(`通关章节：${G.chapter}\n剑术：${G.sword} | 心机：${G.wits} | 魅力：${G.charm}\n生命值：${G.hp}/${G.maxHp}\n做出选择：${(G.choices_made ?? []).length} 次`);
      
      const relLines = [];
      for (const [k, v] of Object.entries(G.relationships)) {
        const npc = NPCS[k];
        if (npc) relLines.push(`${npc.name}：${v > 0 ? '+' : ''}${v}`);
      }
      if (relLines.length) narrate(`人际关系：\n${relLines.join('\n')}`);
      
      narrate(`\n物品：${G.inventory.length > 0 ? G.inventory.join('、') : '无'}`);
      
      divider();
      narrate(`感谢游玩「江湖夜雨」！\n本游戏共有多个不同结局和数百种选择分支。\n尝试不同的选择，发现更多故事吧。`);
    }},
  ]);
};

// ---- 隐藏结局：传承 ----
SCENES['ending_legacy'] = () => {
  G.scene = 'ending';
  clearStory();
  narrate(`你没有拿走天机卷。\n你也没有销毁它。\n\n你做了一件所有人都没想到的事——你把天机卷上的每一个名字，都用暗文重新抄写了一份。\n\n原来的天机卷被封存在落雁峰的山洞里，永远不再开启。\n\n而你抄写的那份——被你分成了十七份，藏在十七个不同的地方。每一份都需要另外两份才能解读。\n\n这不是一份名单了。\n这是一份${hl('传承')}。`);

  divider();
  narrate(`五十年后。\n\n江湖上流传着一个传说：\n\n有一个神秘的组织，专门帮助被追杀的人改换身份、重新开始。他们被称为——${hl('「夜雨帮」')}。\n\n没有人知道他们的总部在哪里。\n没有人知道他们的首领是谁。\n\n但所有走投无路的人都知道——如果在一个雨夜，看到一盏挂在门上的灯笼，灯笼上画着一只孤雁——\n\n那就是安全的。\n\n那里有人会帮你。\n那里有人会记得你。\n\n那里有人相信——每个人都值得第二次机会。`);

  divider();
  narrate(`${hl('你——那个雨夜投宿的无名剑客——\n\n你没有留下名字。\n但你留下了一个传说。\n一个延续了五十年的传说。\n\n也许还会更久。')}\n\n——「江湖夜雨」·隐藏结局 ——`);

  showEnding('传承·夜雨不灭', 'ending_legacy', '最好的结局，是你走后，还有人在你走过的路上继续走下去。');
};
